@router.get("/for-{slug}", response_class=HTMLResponse)
async def industry_landing(request: Request, slug: str):
    ind = INDUSTRIES.get(slug)
    if not ind:
        from fastapi.responses import RedirectResponse
        return RedirectResponse("/", status_code=302)

    # Build list of other industries with short names for cross-linking
    other_industries_data = [INDUSTRIES[s] for s in ind["other_industries"] if s in INDUSTRIES]

    return templates.TemplateResponse(request, "industry.html", {
        "ind": ind,
        "other": other_industries_data,
    })
