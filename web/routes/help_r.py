ARTICLES_BY_SLUG = {a["slug"]: a for a in HELP_ARTICLES}


def _articles_by_category():
    """Группирует статьи по category_slug в порядке CATEGORIES."""
    out = {}
    for cat in CATEGORIES:
        out[cat["slug"]] = [a for a in HELP_ARTICLES if a["category_slug"] == cat["slug"]]
    return out


@router.get("/help", response_class=HTMLResponse)
async def help_index(request: Request):
    return templates.TemplateResponse(
        request=request,
        name="help_index.html",
        context={
            "categories": CATEGORIES,
            "articles_by_cat": _articles_by_category(),
            "total": len(HELP_ARTICLES),
        },
    )


@router.get("/help/{slug}", response_class=HTMLResponse)
async def help_article(request: Request, slug: str):
    article = ARTICLES_BY_SLUG.get(slug)
    if not article:
        return HTMLResponse(
            "<h1>404</h1><p>Статья не найдена. <a href='/help'>Все статьи</a></p>",
            status_code=404,
        )
    # Следующая статья в той же категории
    cat_articles = [a for a in HELP_ARTICLES if a["category_slug"] == article["category_slug"]]
    idx = cat_articles.index(article)
    next_article = cat_articles[idx + 1] if idx + 1 < len(cat_articles) else None
    return templates.TemplateResponse(
        request=request,
        name="help_article.html",
        context={"article": article, "next_article": next_article},
    )
