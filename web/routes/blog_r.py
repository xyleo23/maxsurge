ARTICLES_BY_SLUG = {a["slug"]: a for a in ARTICLES}


@router.get("/", response_class=HTMLResponse)
async def blog_index(request: Request):
    return templates.TemplateResponse(request=request, name="blog_index.html", context={
        "articles": ARTICLES,
    })


@router.get("/{slug}", response_class=HTMLResponse)
async def blog_article(request: Request, slug: str):
    article = ARTICLES_BY_SLUG.get(slug)
    if not article:
        return HTMLResponse(
            "<h1>404</h1><p>Статья не найдена. <a href='/blog/'>Все статьи</a></p>",
            status_code=404,
        )
    return templates.TemplateResponse(request=request, name="blog_article.html", context={
        "article": article,
    })
